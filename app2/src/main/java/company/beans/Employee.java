package company.beans;

public class Employee {

	private int id;
	private String name;
	private int salary;

	/**
	 * @return
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return
	 */
	public int getSalary() {
		return salary;
	}

	/**
	 * @param salary
	 */
	public void setSalary(int salary) {
		this.salary = salary;
	}

}